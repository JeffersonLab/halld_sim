//-----------------------------------------------------------------------------------------------//
float SetBins(double var,  float varbins[] , const int maxvarbins){

		for (int i = 0; i< maxvarbins ; i ++ ){
			if ( var <= varbins[0] || var > varbins[maxvarbins] ) {
				var = -1;
				break;
			}
			if ( var > varbins[i] && var <= varbins[i+1] ) {
				var = i;  
				break;
			}
		}	
 	
		return var;
}

