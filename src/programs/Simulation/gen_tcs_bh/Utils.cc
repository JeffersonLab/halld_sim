float SetBins2D(double var1, double var2, float varbins[][2][2], const int maxvarbins){ 
        float bin=-1;
        for (int i=0; i<maxvarbins; i++){

                if (var1 > varbins[i][0][0] && var1 <= varbins[i][0][1]){ 
                        if (var2 > varbins[i][1][0] && var2 <= varbins[i][1][1]){ 
                                bin = i;
                                break;
                        }
                }
        }
        return bin;
}
 

